# ==================================================
#               GEOMETRY OPERATIONS
# ==================================================

# ---- Cluster center ----
cluster_center = cluster.mean(axis=0)

# ---- Ligand catechol end ----
lig_end = ligand[O_INDICES].mean(axis=0)

# ---- Outward direction ----
direction = lig_end - cluster_center
direction /= np.linalg.norm(direction)

# ---- Translate ligand outward ----
ligand_out = ligand + DELTA * direction

# ---- Updated ligand end ----
lig_end_out = ligand_out[O_INDICES].mean(axis=0)

# ---- Place second cluster ----
target_position = lig_end_out + CLUSTER_SEP * direction
